package model;

public class Timetable {
	
	private int tno;
	private String day;
	private int time;
	
	public int getTno() {
		return tno;
	}
	public String getDay() {
		return day;
	}
	public int getTime() {
		return time;
	}
	public void setTno(int tno) {
		this.tno = tno;
	}
	public void setDay(String day) {
		this.day = day;
	}
	public void setTime(int time) {
		this.time = time;
	}
	
	
}	
